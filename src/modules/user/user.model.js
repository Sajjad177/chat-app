const { Schema, model } = require("mongoose");

const userRouter = new Schema({
  name: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    lowercase: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const User = model("User", userRouter);
module.exports = User;
